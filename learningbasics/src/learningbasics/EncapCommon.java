package learningbasics;

public class EncapCommon {
	
	private String name; // private = restricted access
    private int age;
	// Setter
	public void setName(String newName) {
	    this.name = newName;
	  }
	
	  // Getter
	  public String getName() {
	    return name;
	  }
	  public void setAge(int newAge) {
		    this.age = newAge;
		  }
		
		  // Getter
		  public int getAge() {
		    return age;
		  }
	  
	  
}



